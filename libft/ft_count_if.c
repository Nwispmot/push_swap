#include "libft.h"

int	ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int j;

	j = 1;
	i = 0;
	while (tab[j])
	{
		if (f(tab[j]) == 1)
			i++;
		j++;
	}
	return (i);
}
