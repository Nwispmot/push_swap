#include "libft.h"

char	*ft_strcat(char *restrict s1, const char *restrict s2)
{
	unsigned int i;
	unsigned int j;

	i = 0;
	j = 0;
	while (s1[i])
		i++;
	while (s2[j])
	{
		s1[i] = s2[j];
		i++;
		j++;
	}
	s1[i] = s2[j];
	return (s1);
}
