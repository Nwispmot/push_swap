#include "libft.h"

int	ft_sqrt(int nb)
{
	int a;

	a = 0;
	while (nb > 0)
	{
		nb = nb + (a * a);
		a++;
		nb = nb - (a * a);
	}
	if (nb == 0)
		return (a);
	else
		return (0);
}
